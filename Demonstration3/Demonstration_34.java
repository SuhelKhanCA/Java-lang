package Demonstration3;
class Point{
    int x;
    int y;
}
public class Demonstration_34 {
    public static void main(String[] args) {
        Point myPoint = new Point(); // Declaring an object of class Point
        // Assign value to myPoint's instance variable
        myPoint.x = 10;
        myPoint.y = 20;

        System.out.println(" x  = " + myPoint.x);
        System.out.println(" y = " + myPoint.y);
        
    }
}
