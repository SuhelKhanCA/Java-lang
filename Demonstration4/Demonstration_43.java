package Demonstration4;

public class Demonstration_43 { // CLI input in java
    public static void main(String[] args) {
        for (int i = 0; i < args.length; i++) {
            System.out.println(args[i] + " ");
        }
        System.exit(0); //  statement in Java is used to terminate the currently running Java Virtual Machine (JVM).
    }
}
